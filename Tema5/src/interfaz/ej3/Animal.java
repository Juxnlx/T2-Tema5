package interfaz.ej3;

/**
 * Esta interfaz implementa los atributo que heredaran todos los animales.
 */
public interface Animal {

	/**
	 * Creamos el metodo comer que implementara cada animal.
	 */
	public void comer();

	/**
	 * Creamos el metodo dormir que implementara cada animal.
	 */
	public void dormir();

	/**
	 * Creamos el metodo hacerRuido que implementara cada animal.
	 */
	public void hacerRuido();
}
